package com.jjpeople.jjteach.web.spring.configuration.mentortemplate.bean;

public class TransactionsModuleBean {
	
	private String exerciseId;
	private String exerciseName;
	private String transactionType;
	private String transactionDate;
	private String transactionReadDate;
	private String author;
	
	
	/**
	 * @return the exerciseId
	 */
	public String getExerciseId() {
		return exerciseId;
	}
	/**
	 * @param exerciseId the exerciseId to set
	 */
	public void setExerciseId(String exerciseId) {
		this.exerciseId = exerciseId;
	}
	public String getExerciseName() {
		return exerciseName;
	}
	public void setExerciseName(String exerciseName) {
		this.exerciseName = exerciseName;
	}
	public String getTransactionType() {
		return transactionType;
	}
	public void setTransactionType(String transactionType) {
		this.transactionType = transactionType;
	}
	public String getTransactionDate() {
		return transactionDate;
	}
	public void setTransactionDate(String transactionDate) {
		this.transactionDate = transactionDate;
	}
	public String getTransactionReadDate() {
		return transactionReadDate;
	}
	public void setTransactionReadDate(String transactionReadDate) {
		this.transactionReadDate = transactionReadDate;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	
	
	
	

}
